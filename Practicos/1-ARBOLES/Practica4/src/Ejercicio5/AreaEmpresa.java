


package Ejercicio5;


public class AreaEmpresa {
    private String area;
    private int retardo;

    public AreaEmpresa(String area, int retardo) {
        this.area = area;
        this.retardo = retardo;
    }
    
    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public int getRetardo() {
        return retardo;
    }

    public void setRetardo(int retardo) {
        this.retardo = retardo;
    }
    
    
    
}
